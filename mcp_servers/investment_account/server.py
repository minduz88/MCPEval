@mcp.tool()
def C11T003_tool(
    고객번호: int, 종목코드: str, 주문구분: str, 주문수량: int, 주문가격: int = None
) -> dict:
    """
    주식 주문을 실행합니다.
    (HMA Gateway C11T003_tool 원본 로직 그대로 복사)
    """
    cust_no = str(고객번호).zfill(6)
    current_time = datetime.now().strftime("%Y%m%d%H%M%S")

    # 고객번호 유효성 검사
    valid_customers = ["000001", "000002", "000003"]
    if cust_no not in valid_customers:
        return {"error": "존재하지 않는 고객번호입니다."}

    # 종목별 현재가 (시뮬레이션용)
    current_prices = {
        "A005930": 68000,  # 삼성전자
        "A000660": 135000,  # SK하이닉스
        "A035420": 170000,  # NAVER
        "A035720": 92000,  # LG전자
        "A051910": 420000,  # LG화학
        "A068270": 170000,  # 셀트리온
    }

    current_price = current_prices.get(종목코드, 50000)

    # 주문가격 설정 (시장가 주문인 경우 현재가 사용)
    order_price = 주문가격 if 주문가격 else current_price

    # 주문 시뮬레이션
    order_amount = order_price * 주문수량

    # 주문번호 생성 (시뮬레이션)
    order_no = f"ORD{current_time}{cust_no[-3:]}"

    # 수수료 계산 (0.015%)
    commission = int(order_amount * 0.00015)

    # 주문 결과
    order_result = {
        "주문번호": order_no,
        "고객번호": int(cust_no),
        "계좌번호": f"{cust_no[:4]}-{cust_no[4:6]}-{종목코드[-6:]}",
        "종목코드": 종목코드,
        "주문구분": 주문구분,
        "주문수량": 주문수량,
        "주문가격": order_price,
        "주문금액": order_amount,
        "수수료": commission,
        "실제주문금액": (
            order_amount + commission
            if 주문구분 == "매수"
            else order_amount - commission
        ),
        "주문상태": "접수완료",
        "주문시간": current_time,
        "예상체결가": current_price,
        "체결여부": "미체결",
    }

    return order_result


